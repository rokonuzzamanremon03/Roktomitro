/* ======== ডাইনামিক ড্রপডাউন ফাংশনালিটি ======== */
document.addEventListener("DOMContentLoaded", () => {
    const divisionSelect = document.getElementById("division-search");
    const districtSelect = document.getElementById("district-search");
    const upazilaSelect = document.getElementById("upazila-search");

    // প্রথমে বিভাগগুলো লোড করুন
    bdLocations.divisions.forEach(division => {
        const option = document.createElement("option");
        option.value = division.id;
        option.textContent = division.name_bn; // বাংলা নাম দেখাচ্ছি
        divisionSelect.appendChild(option);
    });

    // বিভাগ পরিবর্তন হলে জেলা লোড করুন
    divisionSelect.addEventListener("change", () => {
        const selectedDivisionId = divisionSelect.value;

        // জেলা ও উপজেলা রিসেট করুন
        districtSelect.innerHTML = '<option value="">-- জেলা সিলেক্ট করুন --</option>';
        upazilaSelect.innerHTML = '<option value="">-- প্রথমে জেলা সিলেক্ট করুন --</option>';
        districtSelect.disabled = true;
        upazilaSelect.disabled = true;

        if (selectedDivisionId) {
            // নির্বাচিত বিভাগের জেলাগুলো ফিল্টার করুন
            const districts = bdLocations.districts.filter(d => d.division_id === selectedDivisionId);

            // জেলা ড্রপডাউনে অপশন যোগ করুন
            districts.forEach(district => {
                const option = document.createElement("option");
                option.value = district.id;
                option.textContent = district.name_bn;
                districtSelect.appendChild(option);
            });
            districtSelect.disabled = false; // জেলা ড্রপডাউন সচল করুন
        }
    });

    // জেলা পরিবর্তন হলে উপজেলা লোড করুন
    districtSelect.addEventListener("change", () => {
        const selectedDistrictId = districtSelect.value;

        // উপজেলা রিসেট করুন
        upazilaSelect.innerHTML = '<option value="">-- উপজেলা সিলেক্ট করুন --</option>';
        upazilaSelect.disabled = true;

        if (selectedDistrictId) {
            // নির্বাচিত জেলার উপজেলাগুলো ফিল্টার করুন
            const upazilas = bdLocations.upazilas.filter(u => u.district_id === selectedDistrictId);

            if (upazilas.length > 0) {
                 // উপজেলা ড্রপডাউনে অপশন যোগ করুন
                upazilas.forEach(upazila => {
                    const option = document.createElement("option");
                    option.value = upazila.id;
                    option.textContent = upazila.name_bn;
                    upazilaSelect.appendChild(option);
                });
                upazilaSelect.disabled = false; // উপজেলা ড্রপডাউন সচল করুন
            } else {
                // যদি কোনো উপজেলা খুঁজে না পাওয়া যায়
                upazilaSelect.innerHTML = '<option value="">-- কোনো উপজেলা পাওয়া যায়নি --</option>';
                upazilaSelect.disabled = true;
            }
        }
    });

    /* ======== ভিজিটর কাউন্টার (Find Blood Page) ======== */
    // এটি find-blood.html পেজের ফুটারের কাউন্টারকে আপডেট করবে

    const visitCountElement = document.getElementById("visit-count-find-blood");

    // visitCountElement যদি পেজে থাকে তবেই এই কোড চলবে
    if (visitCountElement) {
        let visitCount = localStorage.getItem("roktomitroVisitCount"); // আমরা গ্লোবাল কাউন্টারই ব্যবহার করছি

        if (visitCount) {
            // বেস নাম্বারের সাথে যোগ করে প্রদর্শন
            const displayCount = parseInt(visitCount, 10) + 1000; 
            visitCountElement.textContent = displayCount.toLocaleString('bn-BD');

        } else {
             // যদি কোনো কারণে কাউন্টার সেট না হয়
            visitCountElement.textContent = (1000).toLocaleString('bn-BD');
        }
    }
});